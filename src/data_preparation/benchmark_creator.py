    def _classify_question_type(self, question: str) -> str:
        """Классифицирует тип вопроса"""
        question_lower = question.lower()

        if any(word in question_lower for word in ['сколько', 'число', 'количество']):
            return "количественный"
        elif any(word in question_lower for word in ['когда', 'дата', 'год']):
            return "временной"
        elif any(word in question_lower for word in ['кто', 'какой', 'какая']):
            return "фактический"
        elif any(word in question_lower for word in ['какие', 'перечислите']):
            return "перечислительный"
        else:
            return "общий"

    def _classify_content_type(self, context: str) -> str:
        """Классифицирует тип контента"""
        context_lower = context.lower()

        if any(word in context_lower for word in ['акци', 'капитал']):
            return "финансы"
        elif any(word in context_lower for word in ['деятельность', 'направления']):
            return "деятельность"
        elif any(word in context_lower for word in ['проект', 'трубопровод']):
            return "проекты"
        elif any(word in context_lower for word in ['аудитор', 'реестр']):
            return "управление"
        else:
            return "общая информация"

